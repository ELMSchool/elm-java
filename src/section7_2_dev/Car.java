package section7_2_dev;

public class Car {
	
	String make;
	String color;
	String type;
	
	public Car() {	
		
	}

	public Car(String m) {	
		make = m;	
	}

	
	public Car (String m, String c) {
		make = m;
		color = c;
	}
	
	 
	public Car (String m, String c, String t) {
		make = m;
		color = c;
		type = t;
	}

}
